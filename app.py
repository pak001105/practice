def 연도별시대구분(year):
    if pd.isna(year):
        return "시대 미상"
    year = int(year)
    if year < 1990:
        return "1990년대 이전"
    if year <= 1999:
        return "1990년대"
    if year <= 2009:
        return "2000년대"
    if year <= 2019:
        return "2010년대"
    return "2020년대"


def 태그분리(text):
    text = str(text or "").strip()
    if not text:
        return []
    for sep in ["/", "|", ";"]:
        text = text.replace(sep, ",")
    return [t.strip() for t in text.split(",") if t.strip()]


def 목록문자열(items, limit=None):
    if not items:
        return "-"
    if limit is not None:
        items = items[:limit]
    return ", ".join(items)


def 모든태그포함여부(text, selected_tags):
    tag_set = {t.lower() for t in 태그분리(text)}
    return all(tag.lower() in tag_set for tag in selected_tags)


def 숫자표시(value):
    if pd.isna(value):
        return "-"
    try:
        return f"{int(value):,}"
    except Exception:
        return "-"


def 금액표시(value):
    if pd.isna(value):
        return "-"
    try:
        return f"${int(value):,}"
    except Exception:
        return "-"


def 러닝타임구간(분):
    if pd.isna(분):
        return "미상"
    분 = int(분)
    if 분 < 100:
        return "100분 미만"
    if 분 < 120:
        return "100~119분"
    if 분 < 140:
        return "120~139분"
    return "140분 이상"


